import os
import json

root = r"D:\Vs Code\PROJECT\AIVA\backend\node_modules"
count = 0
for root_dir, dirs, files in os.walk(root):
    if "tsconfig.json" in files:
        file_path = os.path.join(root_dir, "tsconfig.json")
        try:
            # Try parsing it
            content = ""
            with open(file_path, 'r', encoding='utf-8') as f:
                content = f.read()
            
            # Simple check if it includes the broken config
            if "@ljharb/tsconfig" in content:
                print(f"Fixing: {file_path}")
                # Use a slightly safer approach for JSON with potential comments/trailing commas
                lines = content.splitlines()
                new_lines = [l for l in lines if '"extends": "@ljharb/tsconfig"' not in l]
                
                # Write back
                with open(file_path, 'w', encoding='utf-8') as f:
                    f.write("\n".join(new_lines))
                count += 1
        except Exception as e:
            print(f"Skipping {file_path}: {e}")

print(f"Done! Cleaned {count} files.")
