import os

root = r"D:\Vs Code\PROJECT\AIVA\backend\node_modules"
files_to_clean = [
    r"call-bind-apply-helpers\tsconfig.json",
    r"dunder-proto\tsconfig.json",
    r"es-object-atoms\tsconfig.json",
    r"get-proto\tsconfig.json",
    r"gopd\tsconfig.json",
    r"has-symbols\tsconfig.json",
    r"hasown\tsconfig.json",
    r"math-intrinsics\tsconfig.json",
    r"side-channel-list\tsconfig.json",
    r"side-channel-map\tsconfig.json",
    r"side-channel-weakmap\tsconfig.json",
    r"side-channel\tsconfig.json",
]

for f in files_to_clean:
    p = os.path.join(root, f)
    if os.path.exists(p):
        print(f"Clearing extends in {f}")
        with open(p, 'r', encoding='utf-8') as file:
            lines = file.readlines()
        with open(p, 'w', encoding='utf-8') as file:
            for line in lines:
                if '"extends":' not in line:
                    file.write(line)
    else:
        print(f"Not found: {f}")

# Fix logform/tsconfig.json strict mode
logform_p = os.path.join(root, r"logform\tsconfig.json")
if os.path.exists(logform_p):
    print("Fixing strict in logform")
    with open(logform_p, 'r', encoding='utf-8') as file:
        content = file.read()
    if '"strict": false' in content:
        content = content.replace('"strict": false', '"strict": true')
    elif '"strict": true' not in content:
        # insert it
        content = content.replace('"compilerOptions": {', '"compilerOptions": {\n    "strict": true,')
    with open(logform_p, 'w', encoding='utf-8') as file:
        file.write(content)

print("Scrub complete!")
